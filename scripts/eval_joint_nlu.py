"""Span-level (conlleval) and joint-frame eval of the joint NLU model.

Runs api.joint_nlu over the held-out slot corpus and scores it with
api.slot_metrics. This measures agreement with MACHINE-GENERATED weak labels, not
with human judgement -- see the circularity note it prints. It does NOT exercise
the chat pipeline, does NOT use the LLM judge, and does NOT replace
scripts/eval_run_inprocess.py or training/eval_holdout.py.

--baseline scores the EXISTING regex EntityExtractor (api/nlu_engine.py) on the
same gold file, projected onto the slot inventory it can actually express
(document -> document_type, program -> program_name, facility -> campus_place;
it has no campus_name and no grade_value pattern at all, so its recall on those
two types is 0 by construction). Without that comparison there is no honest way
to say whether the joint model beats the regexes it would replace, so run both.

Reads : data/slots/test, models/joint_*
Writes: models/eval_joint.json, data/eval/slot_preds.json, data/eval/slot_failures.json
        (--baseline swaps the three defaults to *_baseline.json so a baseline run
        can never clobber the joint report)

Usage: python scripts/eval_joint_nlu.py [--gold data/slots/test] [--min-f1 0.0]
       python scripts/eval_joint_nlu.py --baseline
"""
import argparse
import json
import os
import statistics
import sys
import time
from datetime import datetime, timezone

ap = argparse.ArgumentParser()
ap.add_argument("--gold", default="data/slots/test")
ap.add_argument("--out", default="models/eval_joint.json")
ap.add_argument("--preds", default="data/eval/slot_preds.json")
ap.add_argument("--failures", default="data/eval/slot_failures.json")
ap.add_argument("--limit", type=int, default=0, help="0 = no limit")
ap.add_argument("--min-f1", type=float, default=0.0, help="span micro F1 gate")
ap.add_argument("--baseline", action="store_true",
                help="score the regex EntityExtractor instead of the joint model")
ap.add_argument("--quiet", action="store_true")
_args = ap.parse_args()

os.environ.setdefault("SEVI_ALLOW_UNVERIFIED_MODELS", "1")
os.environ.setdefault("SEVI_JOINT_NLU", "1")
os.environ.pop("DATABASE_URL", None)

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
sys.path.insert(0, ROOT)
os.chdir(ROOT)

_IMPORT_ERROR = None
try:
    from api.slot_metrics import bio_spans, conlleval_report, evaluate  # noqa: E402
    from api.slot_schema import META_PATH, SCHEMA_VERSION, fold, read_corpus, tokens_of  # noqa: E402
except Exception as _e:  # noqa: BLE001 -- upstream builder artifact may not exist yet
    _IMPORT_ERROR = f"{type(_e).__name__}: {_e}"

JOINT_OUT = ("models/eval_joint.json", "data/eval/slot_preds.json", "data/eval/slot_failures.json")
BASELINE_OUT = ("models/eval_joint_baseline.json", "data/eval/slot_preds_baseline.json",
                "data/eval/slot_failures_baseline.json")

# The regex extractor's entity types, projected onto the slot inventory. The four
# types it also emits (date/fee/time/contact) have no slot counterpart and are
# dropped -- scoring them would invent gold spans that do not exist.
BASELINE_ENTITY_MAP = {
    "document": "document_type",
    "program": "program_name",
    "facility": "campus_place",
}
BASELINE_PRIORITY = ("document_type", "program_name", "campus_place")

CAVEATS = [
    "Gold labels are machine-generated by scripts/build_slot_corpus.py from closed-list "
    "gazetteers. This score measures agreement with that labeler, not with human judgement.",
    "The model cannot exceed the gazetteer that produced its training labels; a high score "
    "here is evidence of successful memorisation, not of generalisation.",
    "{n_gold_spans} gold spans in the test split. At this sample size the 95% interval "
    "on span F1 is roughly +/-10 points.",
    "tag_accuracy is inflated by the O majority class and must never be quoted as the "
    "headline number.",
    "No production behaviour changed: SEVI_JOINT_NLU is off by default and the joint intent "
    "is never allowed to route.",
]
BASELINE_CAVEATS = [
    "This run scored the regex EntityExtractor, not the joint model. Its spans are derived "
    "from api/nlu_engine.py ENTITY_PATTERNS matched against the same gold tokens.",
    "The regex extractor has no campus_name and no grade_value pattern, so its recall on "
    "those two types is 0 by construction, not by failure.",
    "Its document/facility patterns include cue words (document|paper|requirement|file, "
    "facility|building|room|office|campus) that are not spans; those become false positives "
    "here. That is a fair reading of what it would hand a downstream slot consumer.",
]


def baseline_tag(tokens):
    """BIO tags for `tokens` from the regex EntityExtractor. Longest-match-wins,
    left-to-right, non-overlapping, BASELINE_PRIORITY on ties."""
    from api.nlu_engine import EntityExtractor  # noqa: PLC0415 -- pulls numpy; keep off import path

    entities = EntityExtractor.extract(" ".join(tokens))
    candidates = []
    for ent_type, surfaces in entities.items():
        slot_type = BASELINE_ENTITY_MAP.get(ent_type)
        if not slot_type:
            continue
        for surface in surfaces:
            folded = tuple(fold(t) for t in tokens_of(str(surface)))
            if folded:
                candidates.append((slot_type, folded))
    candidates = sorted(
        set(candidates),
        key=lambda c: (-len(c[1]), BASELINE_PRIORITY.index(c[0]), c[1]),
    )

    folded_tokens = [fold(t) for t in tokens]
    tags = ["O"] * len(tokens)
    i = 0
    while i < len(tokens):
        for slot_type, folded in candidates:
            n = len(folded)
            if tuple(folded_tokens[i:i + n]) == folded and all(t == "O" for t in tags[i:i + n]):
                tags[i] = "B-" + slot_type
                for j in range(i + 1, i + n):
                    tags[j] = "I-" + slot_type
                i += n
                break
        else:
            i += 1
    return tags


def joint_predictions(rows):
    """-> (pred_tags, pred_intents, confidences, errors, latencies_ms). errors[i] is
    None or a 'TypeName: msg' string; a failed row is scored as an all-O prediction."""
    from api.joint_nlu import load_joint_filler  # noqa: PLC0415 -- lazy: TF lives in here

    filler = load_joint_filler()
    if filler is None:
        print("[ERROR] could not load models/joint_* -- run: python training/train_joint_nlu.py")
        return None

    pred_tags, pred_intents, confidences, errors, latencies = [], [], [], [], []
    for tokens, _gold_tags, _intent in rows:
        t0 = time.perf_counter()
        try:
            result = filler.predict_tokens(tokens)
            err = None if result else "RuntimeError: predict_tokens returned None"
        except Exception as e:  # noqa: BLE001 -- a broken row must not abort the eval
            result, err = None, f"{type(e).__name__}: {e}"
        latencies.append((time.perf_counter() - t0) * 1000.0)
        if result and len(result.get("bio", [])) == len(tokens):
            pred_tags.append(list(result["bio"]))
            pred_intents.append(result.get("intent") or "")
            confidences.append(float(result.get("confidence") or 0.0))
            errors.append(None)
        else:
            pred_tags.append(["O"] * len(tokens))
            pred_intents.append("")
            confidences.append(0.0)
            errors.append(err or "ValueError: bio length != token count")
    return pred_tags, pred_intents, confidences, errors, latencies


def baseline_predictions(rows):
    """Same shape as joint_predictions. The regex extractor predicts no intent."""
    pred_tags, pred_intents, confidences, errors, latencies = [], [], [], [], []
    for tokens, _gold_tags, _intent in rows:
        t0 = time.perf_counter()
        try:
            tags, err = baseline_tag(tokens), None
        except Exception as e:  # noqa: BLE001
            tags, err = ["O"] * len(tokens), f"{type(e).__name__}: {e}"
        latencies.append((time.perf_counter() - t0) * 1000.0)
        pred_tags.append(tags)
        pred_intents.append("")
        confidences.append(0.0)
        errors.append(err)
    return pred_tags, pred_intents, confidences, errors, latencies


def span_diff(gold_tags, pred_tags):
    """-> (missed, spurious) as [type, start, end] triples."""
    gold, pred = set(bio_spans(gold_tags)), set(bio_spans(pred_tags))
    return ([list(s) for s in sorted(gold - pred)], [list(s) for s in sorted(pred - gold)])


def load_model_meta():
    try:
        with open(META_PATH, encoding="utf-8") as f:
            return json.load(f)
    except Exception:  # noqa: BLE001 -- meta is diagnostic, its absence is not fatal
        return {}


def print_report(result, report, args, latencies, n_errors):
    print("\n===== JOINT NLU SPAN EVAL =====")
    print(f"predictor      : {report['predictor']}")
    print(f"gold           : {report['gold_path']}")
    print(f"utterances     : {result['n_utterances']}")
    print(f"gold spans     : {result['n_gold_spans']}")
    print(f"pred spans     : {result['n_pred_spans']}")
    print(f"all-O gold     : {result['all_O_gold_fraction'] * 100:.1f}% of utterances carry no span")
    if n_errors:
        print(f"failed rows    : {n_errors} (scored as all-O predictions)")

    print("\n===== CONLLEVAL =====")
    print(conlleval_report(result))

    print("\n===== JOINT FRAME =====")
    if result.get("intent_supplied"):
        print(f"intent accuracy: {result['intent_accuracy'] * 100:.1f}%  (ATIS '#' multi-intent convention)")
        print(f"frame accuracy : {result['frame_accuracy'] * 100:.1f}%  (intent correct AND every span exact)")
    else:
        print("intent accuracy: n/a -- this predictor emits no intent")
        print("frame accuracy : n/a -- requires an intent prediction")

    if latencies:
        print("\n===== LATENCY =====")
        print(f"median         : {statistics.median(latencies):.1f} ms/utterance")
        print(f"p95            : {sorted(latencies)[int(0.95 * (len(latencies) - 1))]:.1f} ms/utterance")

    print("\n===== CAVEATS =====")
    for c in report["caveats"]:
        print(f"- {c}")

    print("\n===== GATE =====")
    print(f"span micro f1  : {result['span_micro']['f1']:.4f}  gate {args.min_f1:.4f}  "
          f"{'PASS' if report['passed_gate'] else 'FAIL'}")


def print_comparison(report, other_path):
    """Side-by-side against the counterpart report, if one has been written."""
    try:
        with open(other_path, encoding="utf-8") as f:
            other = json.load(f)
    except Exception:  # noqa: BLE001
        print(f"\n[WARN] no counterpart report at {other_path} -- "
              "run the other mode to get the joint-vs-regex comparison")
        return
    if other.get("gold_path") != report.get("gold_path"):
        print(f"\n[WARN] {other_path} scored {other.get('gold_path')}, not {report['gold_path']} -- not comparable")
        return

    if report["predictor"] == "joint":
        joint, base = report, other
    else:
        joint, base = other, report

    print("\n===== JOINT vs REGEX BASELINE =====")
    print(f"{'metric':<28} {'regex':>9} {'joint':>9} {'delta':>9}")
    rows = [("span micro precision", ("span_micro", "precision")),
            ("span micro recall", ("span_micro", "recall")),
            ("span micro f1", ("span_micro", "f1"))]
    for label, path in rows:
        b, j = base[path[0]][path[1]], joint[path[0]][path[1]]
        print(f"{label:<28} {b:>9.4f} {j:>9.4f} {j - b:>+9.4f}")
    print(f"{'span macro f1':<28} {base['span_macro_f1']:>9.4f} "
          f"{joint['span_macro_f1']:>9.4f} {joint['span_macro_f1'] - base['span_macro_f1']:>+9.4f}")
    for slot_type in sorted(set(base["per_label"]) | set(joint["per_label"])):
        b = base["per_label"].get(slot_type, {}).get("f1", 0.0)
        j = joint["per_label"].get(slot_type, {}).get("f1", 0.0)
        print(f"{'  f1 ' + slot_type:<28} {b:>9.4f} {j:>9.4f} {j - b:>+9.4f}")
    print(f"\n[WARN] {joint['n_gold_spans']} gold spans total. The 95% interval on span F1 at "
          "this sample size is roughly +/-10 points, so any delta below 0.10 is noise, "
          "not a result.")
    print("[WARN] The joint model was trained on labels produced by the same gazetteers it is "
          "scored against; the regex baseline was not. This comparison is biased in the "
          "joint model's favour and cannot settle the question on its own.")


def main() -> int:
    args = _args
    if _IMPORT_ERROR:
        print(f"[ERROR] cannot import the slot modules: {_IMPORT_ERROR}")
        print("[ERROR] expected api/slot_schema.py and api/slot_metrics.py -- not run")
        return 1

    if args.baseline:
        for name, joint_default, baseline_default in zip(
            ("out", "preds", "failures"), JOINT_OUT, BASELINE_OUT
        ):
            if getattr(args, name) == joint_default:
                setattr(args, name, baseline_default)

    if not os.path.exists(args.gold):
        print(f"[ERROR] missing {args.gold} -- run: python scripts/build_slot_corpus.py")
        return 1
    try:
        rows = read_corpus(args.gold)
    except Exception as e:  # noqa: BLE001
        print(f"[ERROR] cannot read {args.gold}: {type(e).__name__}: {e}")
        return 1
    if args.limit:
        rows = rows[: args.limit]
    if not rows:
        print(f"[ERROR] {args.gold} is empty -- not run")
        return 1

    predicted = baseline_predictions(rows) if args.baseline else joint_predictions(rows)
    if predicted is None:
        return 1
    pred_tags, pred_intents, confidences, errors, latencies = predicted

    gold_tags = [tags for _t, tags, _i in rows]
    gold_intents = [intent for _t, _tags, intent in rows]
    result = evaluate(
        gold_tags, pred_tags,
        None if args.baseline else gold_intents,
        None if args.baseline else pred_intents,
    )

    report = {
        "schema_version": SCHEMA_VERSION,
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "gold_path": args.gold,
        "predictor": "regex_baseline" if args.baseline else "joint",
        "model_meta": {} if args.baseline else load_model_meta(),
        "n_utterances": result["n_utterances"],
        "n_gold_spans": result["n_gold_spans"],
        "n_pred_spans": result["n_pred_spans"],
        "all_O_gold_fraction": result["all_O_gold_fraction"],
        "span_micro": result["span_micro"],
        "span_macro_f1": result["span_macro_f1"],
        "per_label": result["per_label"],
        "intent_accuracy": result["intent_accuracy"],
        "frame_accuracy": result["frame_accuracy"],
        "tag_accuracy": result["tag_accuracy"],
        "intent_supplied": result.get("intent_supplied", not args.baseline),
        "n_failed_rows": sum(1 for e in errors if e),
        "median_predict_ms": round(statistics.median(latencies), 3) if latencies else 0.0,
        "min_f1_gate": args.min_f1,
        "passed_gate": result["span_micro"]["f1"] >= args.min_f1,
        "caveats": [c.format(n_gold_spans=result["n_gold_spans"]) for c in CAVEATS]
                   + (BASELINE_CAVEATS if args.baseline else []),
    }

    preds = []
    failures = []
    for i, ((tokens, g_tags, g_intent), p_tags) in enumerate(zip(rows, pred_tags)):
        entry = {
            "i": i,
            "tokens": tokens,
            "gold_bio": g_tags,
            "gold_intent": g_intent,
            "pred_bio": p_tags,
            "pred_intent": pred_intents[i],
            "pred_confidence": round(confidences[i], 4),
            "ok": errors[i] is None,
        }
        if errors[i]:
            entry["error"] = errors[i]
        preds.append(entry)

        missed, spurious = span_diff(g_tags, p_tags)
        intent_wrong = (not args.baseline) and pred_intents[i] != g_intent
        if missed or spurious or intent_wrong:
            failures.append({**entry, "missed": missed, "spurious": spurious})
    failures.sort(key=lambda f: len(f["missed"]) + len(f["spurious"]), reverse=True)

    for path, payload in ((args.out, report), (args.preds, preds), (args.failures, failures)):
        parent = os.path.dirname(path)
        if parent:
            os.makedirs(parent, exist_ok=True)
        with open(path, "w", encoding="utf-8") as f:
            json.dump(payload, f, ensure_ascii=False, indent=2)

    if not args.quiet:
        print_report(result, report, args, latencies, report["n_failed_rows"])
        counterpart = (args.out.replace("_baseline.json", ".json") if args.baseline
                       else args.out.replace(".json", "_baseline.json"))
        print_comparison(report, counterpart)

    print(f"\ndone: {len(rows)} utterances -> {args.out}")
    return 0 if report["passed_gate"] else 1


if __name__ == "__main__":
    raise SystemExit(main())
